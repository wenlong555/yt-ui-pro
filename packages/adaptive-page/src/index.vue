<template>
  <div class="t_adaptive_page">
    <div
      :style="{ width: isShow('leftContent') ? `${leftWidth}px` : '0px' }"
      v-if="isShow('leftContent')"
      class="left_content"
    >
      <div class="left_tree">
        <slot name="leftContent" />
      </div>
    </div>
    <yt-layout-page class="right_content" :style="pageStyle">
      <yt-layout-page-item class="table_search" :style="queryPageStyle">
        <yt-query-condition v-bind="$attrs" ref="TQueryConditionPage">
          <template v-for="(index, name) in slots" #[name]="data">
            <slot :name="name" v-bind="data" />
          </template>
        </yt-query-condition>
      </yt-layout-page-item>
      <yt-layout-page-item class="table_main" :style="tablePageStyle">
        <yt-table v-bind="$attrs" ref="TTablePage">
          <template v-for="(index, name) in slots" #[name]="data">
            <slot :name="name" v-bind="data" />
          </template>
        </yt-table>
      </yt-layout-page-item>
      <slot />
    </yt-layout-page>
  </div>
</template>

<script setup lang="ts" name="YTAdaptivePage">
import TLayoutPage from '../../layout-page/src/index.vue'
import TLayoutPageItem from '../../layout-page-item/src/index.vue'
import TTable from '../../table/src/index.vue'
import TQueryCondition from '../../query-condition/src/index.vue'
import { getCurrentInstance, onMounted, ref, useSlots } from 'vue'
defineProps({
  leftWidth: {
    type: Number,
    default: 260,
  },
   pageStyle: {
    type: Object,
    default: ()=>{},
  },
   queryPageStyle: {
    type: Object,
    default: ()=>{},
  },
   tablePageStyle: {
    type: Object,
    default: ()=>{},
  },
})
const slots = useSlots()
// 判断是否使用漏了某个插槽
const isShow = (name) => {
  return Object.keys(slots).includes(name)
}
// 获取实例方法
const instance: any = getCurrentInstance()
// 获取ref
const TQueryConditionPage: any = ref<HTMLElement | null>(null)
const TTablePage: any = ref<HTMLElement | null>(null)
onMounted(() => {
  const exposedObj = {
    ...TQueryConditionPage.value.$.exposed,
    ...TTablePage.value.$.exposed,
  }
  const entries = Object.entries(exposedObj)
  // console.log('111', entries)
  for (const [key, value] of entries) {
    instance.exposed[key] = value
  }
  // console.log(789, instance)
})
defineExpose({ ...instance.exposed, TQueryConditionPage, TTablePage })
</script>
